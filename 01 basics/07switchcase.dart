void main() {
// switch case
  var option = 2;
  switch (option) {
    case 1:
      print("Option 1");
      break;
    case 2:
      print("Option 2");
      break;
    default:
      print("Invalid option");
  }

  String name = "Andy";
  switch (name) {
    case "Andy":
      print("Hello, Andy");
      break;
    case "Dave":
      print("Hello, Dave");
      break;
    default:
      print("No name");
  }
}
